	public String[] getRedNo(){
		String[] res = new String[6];
		List list = new ArrayList();
		for(int i =0;i<33;i++){
			String no = getRedNo(i+1);
			if(no != null && !"".equals(no)){
				list.add(no);
			}
		}
		list.toArray(res);
		return res;
	}
	public int getType(String no){
		int n = Integer.valueOf(no).intValue();
		if( n>=1 &&  n<=11){
			return 1;
		}else if( n>=12 &&  n<=22){
			return 2;
		}
		return 3;
	}
